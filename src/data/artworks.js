/** @returns {number[]} 오름차순 정렬된 중복 없는 년도 배열 */
export function getYears(artworks) {
  return [...new Set(artworks.map(a => a.year))].sort((a, b) => a - b)
}

/** @returns {Artwork[]} 해당 년도 작품 배열. year가 null이면 전체 반환 */
export function getByYear(artworks, year) {
  if (year === null) return artworks
  return artworks.filter(a => a.year === year)
}

/** @returns {Artwork | undefined} */
export function getById(artworks, id) {
  return artworks.find(a => a.id === id)
}

/** @returns {Artwork | undefined} */
export function getFeatured(artworks) {
  return artworks.find(a => a.featured === true)
}
